TX_CHAR EQU 175DH  
ORG 0000H 
LJMP MAIN_START  
ORG 9000H  
MAIN_START: MOV R4, #08  
MOV A, #01H  
NXT_CH: MOV R6, A  
MOV B, A  
ADD A, #30H 
CJNE A, #3AH, N00  
N00: JC N05 
ADD A, #07H  
N05: MOV SBUF, A 
LCALL UART_TX_CHAR  
MOV A, B 
ADD A, #02H  
DJNZ R4, NXT_CH  
SJMP $ 
ORG 175DH  
UART_TX_CHAR: JNB TI, $ 
CLR TI  
RET  
END
